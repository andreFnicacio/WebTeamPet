    {
        if($request->filled('id_vinculado')){

            $pp = PlanosProcedimentos::find($request->get('id_vinculado'));

            $pp->valor_cliente = $request->get('valor_cliente');
            $pp->valor_credenciado = $request->get('valor_credenciado');

            return $pp->update();


        } else {

            return $this->create($request);

